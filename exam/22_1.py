def f22(x):
    g = x & 0b11100000000000000000000000000000
    f = x & 0b00011000000000000000000000000000
    e = x & 0b00000100000000000000000000000000
    d = x & 0b00000011110000000000000000000000
    c = x & 0b00000000001110000000000000000000
    b = x & 0b00000000000001111111111111100000
    a = x & 0b00000000000000000000000000011111
    return (g >> 20) | (f << 3) | (e >> 23) | (d >> 10) | (c >> 19) | (b << 11) | (a << 4)

