def f22(x):
    e = x & 0b10000000000000000000000000000000
    d = x & 0b01000000000000000000000000000000
    c = x & 0b00111111111111111100000000000000
    b = x & 0b00000000000000000010000000000000
    a = x & 0b00000000000000000001111111111111
    return e >> 1 | d << 1 | c >> 14 | b << 3 | a << 17

print(hex(f22(0x29062414)))
print(hex(f22(0x4a50bad1)))
